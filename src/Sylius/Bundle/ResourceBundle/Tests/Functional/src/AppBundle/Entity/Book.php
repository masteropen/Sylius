namespace AppBundle\Entity;

/**
 * @author Anna Walasek <anna.walasek@lakion.com>
 */
class Book
{
    /**
     * @var string
     */
    private $title;

    /**
     * @return string
     */
    public function getTitle()
    {
        return $this->title;
    }

    /**
     * @param string $title
     */
    public function setTitle($title)
    {
        $this->title = $title;
    }
}
